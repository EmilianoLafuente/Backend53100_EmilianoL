// ¿Qué es NPM?
// NPM refiere a las siglas "Node Package Manager", El cual refiere a un manejador de paquetes de Node. Este
// permite que la comunidad de desarrolladores puedan crear sus propios módulos, para poder subirlos a la
// nube y así otros desarrolladores puedan utilizarlos. Para el trabajo de paqueterías, tendremos un archivo en
// nuestro proyecto llamado package.json
// Pagina oficial de NPM: https://www.npmjs.com/




// ¿Qué es package.json?
// package.json es un archivo que generamos dentro de nuestros proyectos, el cual contendrá distintas
// especificaciones del mismo, cosas como:
// • El nombre de tu proyecto
// • La versión de tu proyecto
// • Algunos scripts para correr el proyecto
// • ¿de qué depende el proyecto?


// Dependencias
// Cuando nuestro proyecto necesita utilizar dependencias de terceros a partir de npm, se añade un nuevo campo a
// nuestro package json llamado "dependencies" el cual contendrá los módulos que tenemos instalados en ese
// proyecto y, por lo tanto, indica que el proyecto necesita de esas dependencias instaladas para poder correr
// correctamente.